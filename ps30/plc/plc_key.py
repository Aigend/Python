def update_plc_struct_data(data, body):
    """
        递归，查找key值，并赋值
    :param data:
    :param body:
    :return:
    """
    if isinstance(data, dict) and isinstance(body, dict):
        for k, val in data.items():
            if isinstance(val, dict) and k[2:] in body.keys():
                update_plc_struct_data(val, body[k[2:]])
            elif isinstance(val, list):
                for i in range(len(val)):
                    t = k[2:] + str(i)
                    if t in body.keys():
                        if isinstance(val[i], tuple) and isinstance(body[t], (int, float, str, bool)):
                            tmp = convert_data_type(body[t], val[i])
                            if tmp:
                                val[i] = tmp
                            else:
                                log.warning(f"{k} not get value in post data")
                        elif isinstance(val[i], dict) and isinstance(body[t], dict):
                            update_plc_struct_data(val[i], body[t])
            elif isinstance(val, tuple):
                tmp = convert_data_type(body.get(k[2:], val[0]), val)
                if tmp:
                    data[k] = tmp
                else:
                    log.warning(f"{k} not get value in post data")


