def convert_data_type(src, dst):
    """
    脚本传输过来的数据和实际的格式不一致，这里需要做转换
    如结构体中的int类型的数据，脚本传输的是字符串
    结构体中的boolean数据，脚本传输的是字符串
    ...

    :param src:
    :param dst:
    :return:
    """
    try:
        if dst[0].__class__ == int:
            return int(src), dst[1]
        elif dst[0].__class__ == bool and src.__class__ in (str, int):
            src = int(src)
            rst = True if src > 0 else False
            return rst, dst[1]
        elif dst[0].__class__ == str and src.__class__ == str:
            return src, dst[1]
    except Exception:
        pass


